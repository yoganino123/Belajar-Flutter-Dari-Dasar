// Switch case

void main() {
  var nilai = "C";

  switch (nilai) {
    case 'A':
      print("Lulus Dengan Baik Sekali");
      break;
    case 'B':
      print("Lulus Dengan Baik");
      break;
    case 'C':
      print("Lulus");
      break;
    case 'D':
      print("Lulus Dengan Kurang");
      break;
    default:
      print("Anda Tidak Mempunyai Nilai");
  }
}
