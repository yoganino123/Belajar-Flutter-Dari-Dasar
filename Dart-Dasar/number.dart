// int = bilangan bulat
// double = bilangan desimal
// desimal menggunakan titik contoh 0.5
// num = bilangan yang bisa dirubah dari bulat ke desima

void main() {
  // int & double
  print("int dan double");
  int number1 = 1;
  double number2 = 2.6;

  print(number1);
  print(number2);

  // num
  print('num');
  num number3 = 9;
  print(number3);
  number3 = 2.5;
  print(number3);
}
