// Operator Aritmatika

void main() {
  var result1 = 10 + 10;
  var result2 = 100 - 10;
  var result3 = -5;
  var result4 = 5;
  var result5 = 5 * 5;
  var result6 = 10 / 3;
  var result7 = 10 ~/ 3;
  var result8 = 10 % 3;

  print(result1);
  print(result2);
  print(result3);
  print(result4);
  print(result5);
  print(result6);
  print(result7);
  print(result8);

  var angka1 = 10;
  var angka2 = 3;
  print('--------------------------------');

  print(angka1 + angka2);
  print(angka1 - angka2);
  print(-angka1 + -angka2);
  print(angka1 * angka2);
  print(angka1 / angka2);
  print(angka1 ~/ angka2);
  print(angka1 % angka2);
}
